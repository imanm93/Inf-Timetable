package selp.inf.timetable;

public class CourseListDisplayItem {

	private String courseLevel;
	private String courseCredits;
	private String courseName;
	private String courseLecturer;
	private String courseDP;
	private String courseURL;
	private String courseDRPS;
	
	public String getCourseLevel() {
		return courseLevel;
	}
	public void setCourseLevel(String courseLevel) {
		this.courseLevel = courseLevel;
	}
	public String getCourseCredits() {
		return courseCredits;
	}
	public void setCourseCredits(String courseCredits) {
		this.courseCredits = courseCredits;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseLecturer() {
		return courseLecturer;
	}
	public void setCourseLecturer(String courseLecturer) {
		this.courseLecturer = courseLecturer;
	}
	public String getCourseDP() {
		return courseDP;
	}
	public void setCourseDP(String courseDP) {
		this.courseDP = courseDP;
	}
	public String getCourseURL() {
		return courseURL;
	}
	public void setCourseURL(String courseURL) {
		this.courseURL = courseURL;
	}
	public String getCourseDRPS() {
		return courseDRPS;
	}
	public void setCourseDRPS(String courseDRPS) {
		this.courseDRPS = courseDRPS;
	}
	
}
