package selp.inf.timetable;

public class TimetableDisplayItem {

	private String courseACRONYM;
	private String timeBUILDING;
	private String venueDESCRIPTION;
	private String venueMAP;
	private String timeROOM;
	private String timeSTART;
	private String timeFINISH;
	private String day;
	private String comment;
	
	public String getCourseACRONYM() {
		return courseACRONYM;
	}
	public void setCourseACRONYM(String courseACRONYM) {
		this.courseACRONYM = courseACRONYM;
	}
	
	public String getTimeBUILDING() {
		return timeBUILDING;
	}
	public void setTimeBUILDING(String timeBUILDING) {
		this.timeBUILDING = timeBUILDING;
	}
	
	public String getTimeROOM() {
		return timeROOM;
	}
	public void setTimeROOM(String timeROOM) {
		this.timeROOM = timeROOM;
	}
	
	public String getTimeSTART() {
		return timeSTART;
	}
	public void setTimeSTART(String timeSTART) {
		this.timeSTART = timeSTART;
	}
	
	public String getTimeFINISH() {
		return timeFINISH;
	}
	public void setTimeFINISH(String timeFINISH) {
		this.timeFINISH = timeFINISH;
	}
	
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getVenueMAP() {
		return venueMAP;
	}
	public void setVenueMAP(String venueMAP) {
		this.venueMAP = venueMAP;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getVenueDESCRIPTION() {
		return venueDESCRIPTION;
	}
	public void setVenueDESCRIPTION(String venueDESCRIPTION) {
		this.venueDESCRIPTION = venueDESCRIPTION;
	}
	
}
